package tingkatan_4.aktiviti;

public class aktiviti_20 {
    public static void main(String[] args) {
        // (A)
        float a = 500.0f / 1000;
        System.out.println("(A) 500g kepada kg: " + a + " kg");

        // (B)
        double b = 0.8 * 1000;
        System.out.println("(B) 0.8km kepada m: " + b + " m");

        // (C)
        double c = 1.7 * 1000;
        System.out.println("(C) 1.7kg kepada g: " + c + " g");

        // (D)
        float d = 400.0f / 1000;
        System.out.println("(D) 400m kepada km: " + d + " km");
    }

    // nota: nilai untuk A dan B mempunyai "f" supaya pengiraan boleh dijalankan dengan tepat.
}